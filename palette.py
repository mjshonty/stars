def color_palette(color):
    if color == "background":
        background_color = (4, 12, 6, 1)
        return background_color
    elif color =="text": 
        text_color = (190, 220, 127, 1)
        return text_color
    elif color == "line":
        line_color = (238, 255, 204, 1)
        return line_color